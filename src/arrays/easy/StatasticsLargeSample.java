package arrays.easy;

public class StatasticsLargeSample {
    public double[] sampleStats(int[] count) {
        double[] ans = new double[5];
        double min = Double.MAX_VALUE;
        double max = 0;
        double sum = 0;
        double size = 0;
        double modeCount = 0;
        double mode = 0;
        for(int i=0; i<256; i++){
            if(count[i]>0){
                if(min == Double.MAX_VALUE){
                    min = i;
                }
                max = i;
                sum += i * count[i];
                size += count[i];
                //Get mode
                if(count[i] > modeCount){
                    modeCount = count[i];
                    mode = i;
                }
            }
        }

        double median = 0;
        if(size % 2 == 0){
            double index = size / 2;
            double temp = 0;
            for(int i=0; i<256; i++){
                if(count[i] > 0){
                    temp += count[i];
                    if(temp == index){
                        median = (double)(i+i+1)/2;
                        break;
                    }else if(temp > index){
                        median = i;
                        break;
                    }
                }
            }
        }else{
            double index = size / 2;
            double temp = 0;
            for(int i=0; i<256; i++){
                if(count[i] > 0){
                    temp += count[i];
                    if(temp >= index){
                        median = i;
                        break;
                    }
                }
            }
        }
        ans[0] = min;
        ans[1] = max;
        ans[2] = sum / size;
        ans[3] = median;
        ans[4] = mode;
        return ans;
    }
}
